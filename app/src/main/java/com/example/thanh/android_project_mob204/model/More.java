package com.example.thanh.android_project_mob204.model;

public class More {
    private int civLogo;
    private String function;

    public More(int civLogo, String function) {
        this.civLogo = civLogo;
        this.function = function;
    }

    public int getCivLogo() {
        return civLogo;
    }

    public void setCivLogo(int civLogo) {
        this.civLogo = civLogo;
    }

    public String getFunction() {
        return function;
    }

    public void setFunction(String function) {
        this.function = function;
    }
}
